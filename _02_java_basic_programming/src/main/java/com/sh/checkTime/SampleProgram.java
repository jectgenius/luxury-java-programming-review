package com.sh.checkTime;

public class SampleProgram {                    // SampleProgram 클래스 정의 시작
    public static void main(String[] args) {    // main() 메소드 정의 시작
        int i;                                  // int형 변수 i 선언
        int j;                                  // int형 변수 j 선언
        i = 10;                                 // 변수 i에 10 대입하여 초기화
        j = 20;                                 // 변수 j에 20 대입하여 초기화
        System.out.println(i+j);                // System.out.println() 메소드 호출하여 i+j 연산결과 출력, 30
    }                                           // main() 메소드 정의 끝
}                                               // SampleProgram 클래스 정의 끝
