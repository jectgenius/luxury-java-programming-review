package com.sh.check.time;

public class P149_3 {                           // P149_3 클래스 정의 시작
    public static void main(String[] args) {    // main() 메소드 정의 시작

        for(int i = 0; i< args.length; i++) {   // int형 변수 i 선언 동시에 0 대입하여 초기화, i가 배열 args의 크기 length보다 작을 동안 반복, i에 +1
            System.out.println(args[i]);        // System.out.println() 메소드 호출하여 배열 args의 i번째 요소 출력
        }                                       // 배열 args의 모든 요소 출력 완료

    }                                           // main() 메소드 정의 끝
}                                               // P149_3 클래스 정의 끝
