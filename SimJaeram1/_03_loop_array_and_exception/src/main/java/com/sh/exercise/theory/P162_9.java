package com.sh.exercise.theory;

public class P162_9 {                       // P162_9 클래스 정의 시작
    public static void main(String[] args) {// main() 메소드 정의 시작
        int myArray[] = new int[2];         // 크기가 2인 int형 배열을 생성하였다.
        myArray[0] = 1;                     // 배열 myArray의 0번째 인덱스 요소에 1대입
        myArray[1] = myArray[0] + 1;        // 배열 myArray의 1번째 인덱스 요소에 myArray[0] + 1 대입
    }                                       // main() 메소드 정의 끝
}                                           // P162_9 클래스 정의 끝
